package com.wilson.sepsisapp.Cards;

public class ResultCard {
    int HR,RR,O2,Temp,Age,Gender,Output;

    public ResultCard() {
    }

    public int getHR() {
        return HR;
    }

    public void setHR(int HR) {
        this.HR = HR;
    }

    public int getRR() {
        return RR;
    }

    public void setRR(int RR) {
        this.RR = RR;
    }

    public int getO2() {
        return O2;
    }

    public void setO2(int o2) {
        O2 = o2;
    }

    public int getTemp() {
        return Temp;
    }

    public void setTemp(int temp) {
        Temp = temp;
    }

    public int getAge() {
        return Age;
    }

    public void setAge(int age) {
        Age = age;
    }

    public int getGender() {
        return Gender;
    }

    public void setGender(int gender) {
        Gender = gender;
    }

    public int getOutput() {
        return Output;
    }

    public void setOutput(int output) {
        Output = output;
    }
}
